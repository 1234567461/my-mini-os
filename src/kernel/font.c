/* 字体大小表（8x16是基础大小） */
#define FONT_WIDTH  8
#define FONT_HEIGHT 16

/* ==========================================
 * 初始化字体系统
 * ========================================== */
void font_init(void) {
    /* 目前使用静态字体数据，无需初始化 */
}

/* ==========================================
 * 获取字符宽度
 * ========================================== */
int font_char_width(int size) {
    /* 基础字体宽度8，乘以缩放因子 */
    return FONT_WIDTH * size;
}

/* ==========================================
 * 获取字符高度
 * ========================================== */
int font_char_height(int size) {
    return FONT_HEIGHT * size;
}

/* ==========================================
 * 绘制单个字符
 * ========================================== */
int font_draw_char(int x, int y, char c, uint32_t color, int size) {
    /* 计算字符在字体表中的索引 */
    int index = (unsigned char)c - 0x20;

    /* 只支持可显示的ASCII字符 */
    if (index < 0 || index > 95) {
        /* 绘制空格 */
        return font_char_width(size);
    }

    const uint8_t *char_data = font_8x16[index];

    /* 按像素绘制 */
    for (int py = 0; py < FONT_HEIGHT; py++) {
        uint8_t row = char_data[py];
        for (int px = 0; px < FONT_WIDTH; px++) {
            /* 提取位（MSB在前） */
            int bit = (row >> (7 - px)) & 1;
            if (bit) {
                /* 绘制缩放后的像素 */
                for (int sy = 0; sy < size; sy++) {
                    for (int sx = 0; sx < size; sx++) {
                        graphics_draw_pixel(x + px * size + sx, y + py * size + sy, color);
                    }
                }
            }
        }
    }

    return font_char_width(size);
}

/* ==========================================
 * 绘制字符串
 * ========================================== */
int font_draw_string(int x, int y, const char *str, uint32_t color, int size) {
    int cursor_x = x;
    int char_width = font_char_width(size);

    while (*str) {
        if (*str == '\n') {
            /* 换行 */
            cursor_x = x;
            y += font_char_height(size);
        } else if (*str == '\t') {
            /* Tab，跳过4个空格 */
            cursor_x += char_width * 4;
        } else {
            cursor_x += font_draw_char(cursor_x, y, *str, color, size);
        }
        str++;
    }

    return cursor_x - x;  /* 返回字符串宽度 */
}

/* ==========================================
 * 测量字符串宽度
 * ========================================== */
int font_measure_string(const char *str, int size) {
    int width = 0;

    while (*str) {
        if (*str == '\n' || *str == '\t') {
            width += font_char_width(size);
        } else {
            width += font_char_width(size);
        }
        str++;
    }

    return width;
}